tup = (0,1,4,3)

for i in range(len(tup)):
    if(tup[i] == i):
        print("Ο αριθμός",tup[i],"αντιστοχεί στον δείκτη της θέσης του.")
    else:
        print("Ο αριθμός",tup[i],"δεν αντιστοχεί στον δείκτη της θέσης του.")