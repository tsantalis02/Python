d = {'a': 1, 'b': 2, 'c': 3, 'd': 4, 'e': 5}
l = list()
for key,val in d.items():
    l.append(val)
print("Η μέγιστη τιμή του λεξικού είναι",max(l))
print("Η ελάχιστη τιμή του λεξικού είναι",min(l))    