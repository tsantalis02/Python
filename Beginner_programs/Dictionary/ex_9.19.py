d = {'a': [0,1], 'b': [2,3,4], 'c': [5,6,7,8]}
l_01 = list()
l_02 = list()
for key,val in d.items():
    l_01.append(key)
    l_02.append(len(d[key]))   
d_new = dict(zip(l_01,l_02))
print(d_new)    