def CtoF(l):
    f = list()
    for i in l:
        fahr = i * (9 / 5) + 32
        f.append(fahr)
    return f

c = [23,40,34,17]
f = CtoF(c)
print("Μετατροπή από Κελσίου")
print(c)
print("Σε Φαρενάιτ")
print(f)    