x = int(input("Δώσε αριθμό δευτερολέπτων: "))
print("Τα",x,"δευτερόλεπτα αντιστοιχούν σε:",end=" ")
h = x // 3600
x = x - h*3600
m = x // 60
x = x - m*60
s = x % 60
print(h,"ώρες,",m,"λεπτά και",s,"δευτερόλεπτα.")


